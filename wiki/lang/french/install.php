    <h2 class="formatter-title">Ressources utilisées</h2>
    Vous pourrez retrouver ici la liste des projets et/ou Logiciels Libres que nous avons utilisé dans PHPBoost. Merci à toutes les personnes 
    qui s\'investissent dans ces différents projets et nous fournissent des outils de qualité, sur lesquels nous pouvons nous appuyer pour 
    proposer à notre tour notre projet.

    <h4 class="formatter-title">Font-Awesome</h4>
    La librairie <a class="offload" href="https://fortawesome.github.io/Font-Awesome">Font-Awesome</a> est un projet d\'icônes vectorielles 
    évolutives qui peuvent instantanément être personnalisés en taille, couleur, ombre et tout ce qui peut être fait en CSS. L\'utilisation 
    des icônes vectorielles a permis de réduire la quantité d\'image nécessaire sur PHPBoost.

    <h4 class="formatter-title">Font-Awesome-Animation</h4>
    La librairie <a class="offload" href="https://l-lin.github.io/font-awesome-animation/">Font-Awesome-Animation</a> est un projet basé sur 
    la librairie Font-Awesome. Elle permet d\'ajouter un certain nombre d\'animation les icônes.

    <h4 class="formatter-title">TinyMCE</h4>
    <a class="offload" href="https://www.tinymce.com/">TinyMCE</a> est un éditeur HTML WYSIWYG (What You See Is What You Get) développé en 
    JavaScript.<br />
    Cet éditeur transforme un champ de formulaire web en une zone d\'édition dotée d\'une barre d\'icônes. Cette zone peut ensuite être 
    utilisée comme un traitement de texte pour produire du code HTML sans en connaître la syntaxe.

    <h4 class="formatter-title">PCLZIP</h4>
    Développé par PHPConcept, <a class="offload" href="https://www.phpconcept.net/pclzip/">PCLZIP</a> est une librairie permettant de 
    travailler sur des archives au format Zip en PHP. Elle est utilisée à plusieurs reprises dans PHPBoost.

    <h4 class="formatter-title">GeSHi</h4>
    <a class="offload" href="https://qbnz.com/highlighter/">GeSHi</a> (Generic Syntax Highlighter) est un script PHP qui permet de colorer 
    syntaxiquement de nombreux langages informatiques.

    <h4 class="formatter-title">jQuery</h4>
    <a class="offload" href="https://jquery.com">jQuery</a> est une bibliothèque (ou Framework) JavaScript et AJAX offrant des effets 
    visuels dynamiques et des éléments d\'interface utilisateur.

    <h4 class="formatter-title">PHPMathPublisher</h4>
    <a class="offload" href="https://www.xm1math.net/phpmathpublisher/index_fr.html">PHPMathPublisher</a> est un ensemble de fonctions 
    permettant de mettre en forme des formules mathématiques à partir d\'une syntaxe proche de celle du 
    <a class="offload" href="https://fr.wikipedia.org/wiki/LaTeX">LaTeX</a>.

    <h4 class="formatter-title">Lightcase</h4>
    <a class="offload" href="https://cornel.bopp-art.com/lightcase">Lightcase</a> est une lightbox responsive.

    <h4 class="formatter-title">CssMenuMaker</h4>
    <a class="offload" href="https://github.com/cssmenumaker/jQuery-Plugin-Responsive-Drop-Down">Cssmenumaker</a> permet de créer des menus 
    en responsive pour qu\'ils s\'adaptent à n\'importe quel support.

    <h4 class="formatter-title">BasicTable</h4>
    <a class="offload" href="https://www.jerrylow.com/basictable">BasicTable</a> permet de créer des tableaux en responsive pour 
    qu\'ils s\'adaptent à n\'importe quel support.
